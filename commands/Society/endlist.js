export default {
    command: Array.from({ length: 300 }, (_, i) => `end${i + 1}`).concat(['endlist']),
    category: 'grupo',
    isAdmin: true,

    run: async (client, m) => {
        if (!m.isGroup) return client.reply(m.chat, '⚠️ Solo en grupos.', m)

        // --- FUNCIÓN CATÁLOGO DIVIDIDO EN 3 ---
        if (m.command === 'endlist') {
            const keys = Object.keys(separadores);
            const total = keys.length;
            const size = 100;

            for (let i = 0; i < total; i += size) {
                let bloqueNum = (i / size) + 1;
                let list = `*👑 EMPIRE BOT - CATÁLOGO [${bloqueNum}/3] 👑*\n\n`;
                
                const section = keys.slice(i, i + size);
                section.forEach(id => {
                    // Reemplaza ✨ y %emoji% para la vista previa en el catálogo
                    list += `*${id}.* ${separadores[id].replace(/%emoji%|✨/g, '✨')}\n`;
                });

                await client.reply(m.chat, list.trim(), m);
                
                // Espera 1.5s entre bloques para evitar saturación
                if (i + size < total) await new Promise(r => setTimeout(r, 1500));
            }
            return;
        }

        // --- FUNCIÓN CIERRE LIMPIO ---
        const cmd = m.command;
        const body = m.text || '';
        const textAfter = body.slice(body.indexOf(cmd) + cmd.length).trim();
        const emoji = textAfter.split(' ')[0] || '🌟';

        const quoted = m.quoted ? m.quoted : null;
        if (!quoted) return client.reply(m.chat, '⚠️ Responde a la lista de revisión para cerrarla.', m);

        const idNum = cmd.replace('end', '');
        const marco = separadores[idNum]?.replace(/%emoji%|✨/g, emoji);
        if (!marco) return;

        // 1. PARTICIPANTES
        const metadata = await client.groupMetadata(m.chat);
        const botNumber = client.user.id.split(':')[0] + '@s.whatsapp.net';
        const allParticipants = metadata.participants.map(p => p.id).filter(id => id !== botNumber);
        const admins = metadata.participants.filter(p => p.admin).map(p => p.id);

        // 2. FILTRAR MENCIONES DEL MENSAJE RESPONDIDO
        const alDiaJids = [...new Set((quoted.mentionedJid || []).filter(id => id !== botNumber))];
        
        // 3. PENDIENTES (Miembros que no están en menciones ni son admins)
        const pendientesJids = allParticipants.filter(id => 
            !alDiaJids.includes(id) && !admins.includes(id)
        );

        // 4. CONSTRUCCIÓN DEL MENSAJE (Estructura Judai)
        let mensaje = `\n${marco}\n\n*✅ AL DÍA*\n`;
        mensaje += alDiaJids.length > 0 ? alDiaJids.map(v => `${emoji}┃@${v.split('@')[0]}`).join('\n') : `_Vacío_`;

        mensaje += `\n\n${marco}\n\n*⏳ PENDIENTES*\n`;
        mensaje += pendientesJids.length > 0 ? pendientesJids.map(v => `${emoji}┃@${v.split('@')[0]}`).join('\n') : `*¡Todos al día! ✨*`;

        mensaje += `\n\n${marco}\n\n*👑 ADMINISTRACIÓN*\n`;
        mensaje += admins.map(v => `${emoji}┇@${v.split('@')[0]}`).join('\n');
        
        mensaje += `\n\n${marco}`;

        // 5. ENVÍO CON MENCIONES
        return client.reply(m.chat, mensaje.trim(), m, { 
            mentions: [...alDiaJids, ...pendientesJids, ...admins] 
        });
    }
}
